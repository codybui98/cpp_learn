#include<iostream>
using namespace std;

int main() {
    int m, n;
    cout << "Hay nhap vao 2 so n, m: ";
    cin >> m >> n;
    cout << m << "+" << n << "=" << m+n << endl;
    cout << m << "-" << n << "=" << m-n << endl;
    cout << m << "*" << n << "=" << m*n << endl;
    cout << m << "/" << n << "=" << (float)m/n << endl;
    return 0;
}
